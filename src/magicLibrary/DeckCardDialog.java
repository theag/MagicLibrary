    private DeckCardDialog(java.awt.Frame parent, Card card) {
        super(parent, true);
        initComponents();
        setLocationRelativeTo(parent);
        this.getContentPane().setBackground(Color.white);
        saved = false;
        lblName.setText(card.name);
        lblType.setText(card.getAllTypeString());
        if(!lblType.getText().contains("Land")) {
            ManaPanel pnlMana = new ManaPanel(card.manaCost);
            pnlManaHolder.add(pnlMana, BorderLayout.LINE_END);
        }
        lblText.setText(String.format("<html><div style=\"width:%dpx;\">%s</div><html>", lblText.getWidth(), card.text));
        if(card.loyalty != null) {
            lblPTL.setText(card.loyalty);
        } else if(card.power != null) {
            lblPTL.setText(card.power +"/" +card.toughness);
        } else {
            lblPTL.setText("");
        }
        spnCount.setValue(card.count);
        lblUpdated.setText("Last Updated: " +card.formatUpdate());
        //todo: lblDecks.setText(String.format("<html><div style=\"width:%dpx;\">%s</div><html>", lblDecks.getWidth(), card.getDeckString()));
        txtNotes.setText(card.notes);
    }

