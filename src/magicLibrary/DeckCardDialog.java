    public static void showDialog(java.awt.Frame parent, Card card) {
        DeckCardDialog cd = new DeckCardDialog(parent, card);
        cd.pack();
        cd.setVisible(true);
        if(cd.saved) {
            card.notes = cd.txtNotes.getText();
        }
        cd.dispose();
    }
    
